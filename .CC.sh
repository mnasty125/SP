#!/bin/bash

read n
regex1="^([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\$"
regex2="([a-f0-9]{1,4}:){7}([a-f0-9]{1,4})\$"
for ((i=0; i<n; i++))
do
    read line
    if [[ $line =~ $regex1 ]]
    then
        echo "IPv4"
    elif [[ $line =~ $regex2 ]]
    then
        echo "IPv6"
    else
        echo "Neither"
    fi
done
